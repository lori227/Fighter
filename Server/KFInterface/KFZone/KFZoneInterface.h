#ifndef __KF_ZONE_INTERFACE_H__
#define __KF_ZONE_INTERFACE_H__

#include "KFrame.h"

namespace KFrame
{
    // 分区设置
    class KFZone
    {
    public:
        // 游戏服名字
        std::string _name;

        // 游戏分区id
        uint32 _id = 0;

        // 逻辑id
        uint32 _logic_id = 0;
    };

    /////////////////////////////////////////////////////////////////////////////////////////////
    class KFZoneInterface : public KFModule
    {
    public:
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // 游戏分区
        virtual const KFZone* GetZone() = 0;

        // 游戏分区信息
        virtual const KFZone* GetZone( uint32 zoneid ) = 0;

        // 判断游戏分区id
        virtual bool IsServerSameZone( uint64 serverid ) = 0;
        virtual bool IsPlayerSameZone( uint64 playerid ) = 0;
    };

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    __KF_INTERFACE__( _kf_zone, KFZoneInterface );
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}



#endif